<template>
	<CSidebar 
		fixed 
		:minimize="minimize"
		:show="show"
		@update:show="(value) => $store.commit('set', ['sidebarShow', value])"
	>
		<CSidebarBrand class="d-md-down-none" to="/">
			<CIcon 
				class="c-sidebar-brand-full" 
				name="logo" 
				size="custom-size" 
				:height="35" 
				viewBox="0 0 556 134"
			/>
			<CIcon 
				class="c-sidebar-brand-minimized" 
				name="logo" 
				size="custom-size" 
				:height="35" 
				viewBox="0 0 110 134"
			/>
		</CSidebarBrand>

		<CRenderFunction flat :content-to-render="menu"/>
		<CSidebarMinimizer
			class="d-md-down-none"
			@click.native="$store.commit('set', ['sidebarMinimize', !minimize])"
		/>
	</CSidebar>
</template>

<script>
import generator from '../helpers/menuGenerator.js'

export default {
	name: 'TheSidebar',
	computed: {
		show () {
			return this.$store.state.sidebarShow 
		},
		minimize () {
			return this.$store.state.sidebarMinimize 
		},
	},
	data() {
		return {
			menu: []
		}
	},
	mounted() {
		this.menu = generator.generateMenu()
	}
}
</script>

